	"\e[0;35m"
	"\e[0;31m"
)

# default terminal text color
DEF="\e[0m"

# function that prints the pirate-parrot
pirate_parrot()
{
    while [ 1 ]
    do
		idx=0
        for parrot in "${frames[@]}"
        do
			# picks color according to frame number
			curr_color=${colors[idx]}

			# sets cursor at the 0,0 coordinate
			tput cup 0 0
			
			# writes the art and the message
            echo -e "$curr_color$parrot$DEF"
            echo -e "\n\n\t\t\tYOU JUST GOT ATTACKED BY A PIRATE FROM THE ORDER!!!"

            sleep 0.08
			((idx++))

        done
    done
}

# leave alternate screen view and return cursor to normal when exiting
exit_loop()
{
	tput rmcup
	tput cnorm
}

# main execution

trap exit_loop exit # trap exit function
tput smcup # switch to alternate screen view
tput civis # cursor invisible
stty -echo # dont write user input
pirate_parrot # parrot !!